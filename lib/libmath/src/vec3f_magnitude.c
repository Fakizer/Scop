#include "libmath.h"

float	vec3f_magnitude(t_vec3f v)
{
	return (sqrt(v.vec3f[0] * v.vec3f[0] + v.vec3f[1] * v.vec3f[1] +
				v.vec3f[2] * v.vec3f[2]));
}
