#include "libmath.h"

float	vec3f_dot(t_vec3f a, t_vec3f b)
{
	return (a.vec3f[0] * b.vec3f[0] + a.vec3f[1] * b.vec3f[1] +
			a.vec3f[2] * b.vec3f[2]);
}
