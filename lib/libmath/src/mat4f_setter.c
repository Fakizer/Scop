#include "libmath.h"

void		mat4f_setter(t_mat4f *m, float f)
{
	int		i;

	i = -1;
	while (++i < 16)
	{
		if (f == IDENTITY)
			m->mat4f[i] = (i % 5 == 0 ? 1 : 0);
		else
			m->mat4f[i] = f;
	}
}
